import request from '@/utils/request';

export async function queryWarehouseType(params) {
  return request('/api/wms/storageType/list', {
    method: 'POST',
    headers:{
      'token': window.localStorage.getItem('token'),
      ...request.headers,
    },
    data: {
      ...params,
    },
  });
}

