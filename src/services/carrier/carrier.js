import request from '@/utils/request';

export async function queryCarrier(params) {
  return request('/api/wms/carrier/list', {
    method: 'POST',
    headers:{
      'token': window.localStorage.getItem('token'),
      ...request.headers,
    },
    data: {
      ...params,
    },
  });
}

